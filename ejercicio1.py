# Definir el diccionario
diccionario = {
    'a': 10,
    'b': 5,
    'c': 8,
    'd': 12,
    'e': 3
}

# Obtener los valores del diccionario y ordenarlos de manera ascendente
valores_ordenados = sorted(diccionario.values())

# Imprimir los valores ordenados
print("Valores del diccionario en orden ascendente:")
for valor in valores_ordenados:
    print(valor)
